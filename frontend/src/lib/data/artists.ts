export function getArtistByMbid(mbid: string): Artist | undefined {
	return artists.find((a) => a.mbid === mbid);
}

export function getSimilarArtists(mbid: string): Artist[] {
	const artist = getArtistByMbid(mbid);
	if (!artist) return [];
	return artist.similarMbids
		.map((id) => artists.find((a) => a.mbid === id))
		.filter((a): a is Artist => !!a);
}

